// 手机号码正则表达式
export const telphoneRegExp = /^1[3-9]\d{9}$/

// 电子邮箱正则表达式
export const emailRegExp =
  /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/

// 中文姓名正则表达式 (增强版-支持生僻字和少数民族姓名)
export const chineseNameRegExp = /^[\u3400-\u9fa5·•]{2,15}$/
